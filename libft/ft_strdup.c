#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*p1;

	i = 0;
	p1 = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (!p1)
		return (0);
	while (s1[i])
	{
		p1[i] = s1[i];
		i++;
	}
	p1[i] = '\0';
	return (p1);
}
